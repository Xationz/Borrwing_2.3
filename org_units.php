/**
 * Get the label for a given unit code
 */
function getOrgUnitLabel($code) {
    global $BORROWER_UNITS;
    foreach ($BORROWER_UNITS as $unit) {
        if ($unit['code'] === $code) {
            return $unit['label'];
        }
    }
    return $code;
}

/**
 * Get the name for a given unit code
 */
function getOrgUnitName($code) {
    global $BORROWER_UNITS;
    foreach ($BORROWER_UNITS as $unit) {
        if ($unit['code'] === $code) {
            return $unit['name'];
        }
    }
    return $code;
}
